use super::chunk_type::ChunkType;
use std::convert::TryFrom;
use std::fmt;

struct Chunk {
    length: u32,
    chunk_type: ChunkType,
    crc: u32,
    data: Vec<u8>,
}

impl Chunk {
    fn length(&self) -> u32 {
        self.length
    }

    fn chunk_type(&self) -> &ChunkType {
        &self.chunk_type
    }

    fn data(&self) -> &[u8] {
        &self.data
    }

    fn crc(&self) -> u32 {
        self.crc
    }

    fn data_as_string(&self) -> String {
        self.data.iter().map(|&x| x as char).collect()
    }

    fn as_bytes(&self) -> Vec<u8> {
        self.data.clone()
    }
}

